package day19_ForLoop;

public class PushUpLoop {
    public static void main(String[] args) {
        for (int i = 1; i <= 500; i++) {
            System.out.print("Push Up " + i+ " ");
        }

        System.out.println();

        for (char a = 97; a <= 122; a++) {
            System.out.print(a + " ");
        }
        System.out.println();

        for (char A = 65; A <= 90; A++) {
            System.out.print(A + " ");
        }
        System.out.println();

        for (char c = 1; c <= 50; c++) {
            System.out.print(c + " ");
        }


    }
}


