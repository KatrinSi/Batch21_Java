package day19_ForLoop;

public class ForLoopIntro {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println("hello");
        }

        System.out.println("========================");
        String name = "KatrinSi";
        for (int i = 5; i > 0; i--) { //i:5 ; i:4; i:3; i:2; i:1
            System.out.println(name);
        }
    }
}
