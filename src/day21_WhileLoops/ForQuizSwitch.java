public class ForQuizSwitch {
    public static void main(String[] args) {

        int i = 10;

        switch (i){
            case 10:
                System.out.println("Russia");
                break;
            case 11:
                System.out.println("USA");
                break;
            default:
                System.err.println("Invalid input");



        }


    }
}
