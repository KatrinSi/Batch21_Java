package day21_WhileLoops;

public class WhileLoopPractice {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++){
            System.out.println(i);
        }
        int j =1;
        while(j <= 10) {
            System.out.println(j);

            j++;//after the print statement!!
        }

        System.out.println("===========================");


    }
}
